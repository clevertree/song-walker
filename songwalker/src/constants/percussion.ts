export function getPercussionInfo(shortName: string) {
    const percInfo = percussionMap[shortName];
    if (!percInfo)
        throw new Error("Percussion name not found: " + shortName)
    const [keyNumber, keyRange, name] = percInfo;
    return {keyNumber, keyRange, name}
}
